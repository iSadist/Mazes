    private func getRandomColor() -> UIColor {
        let colors = [UIColor.brown, UIColor.cyan, UIColor.white, UIColor.orange, UIColor.gray]
        
        let randomIndex = arc4random_uniform(UInt32(colors.count))
        return colors[Int(randomIndex)]
